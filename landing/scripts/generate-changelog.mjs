#!/usr/bin/env node
// Тянет последние 3 GitHub-релиза и пишет src/generated/changelog.ts.
// Запускается в prebuild/postinstall (см. package.json). Сетевая ошибка
// не валит билд — пишем пустой массив, секция changelog просто не отрисуется.
import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO = 'create-user-interface/createui-icons';
const COUNT = 3;

const here = dirname(fileURLToPath(import.meta.url));
const out = resolve(here, '../src/generated/changelog.ts');

async function fetchReleases() {
  const headers = { Accept: 'application/vnd.github+json' };
  // GH Actions runner получает GITHUB_TOKEN автоматически и снимает unauth-rate-limit.
  // Локально fallback на анонимный запрос (60/час с IP).
  if (process.env.GITHUB_TOKEN) {
    headers.Authorization = `Bearer ${process.env.GITHUB_TOKEN}`;
  }
  const r = await fetch(
    `https://api.github.com/repos/${REPO}/releases?per_page=${COUNT}`,
    { headers },
  );
  if (!r.ok) throw new Error(`GitHub API ${r.status}: ${await r.text()}`);
  return r.json();
}

// Достаём первый смысловой абзац: всё до первого `## `-заголовка или `---`-разделителя.
// Этого достаточно — sync.yml пишет лидом «Synced from … → …» (sync) или
// «Patch release — …» (patch), и оба варианта самодостаточны.
function extractSummary(body) {
  const lines = body.split('\n');
  const out = [];
  for (const line of lines) {
    if (line.startsWith('## ') || line.startsWith('---')) break;
    out.push(line);
  }
  return out.join('\n').trim();
}

// Считает иконки по заголовкам `## Added (N)` / `## Modified (N)` / etc.
// Возвращает {} для patch-релизов, где этих секций нет.
function extractCounts(body) {
  const counts = {};
  const re = /^## (Added|Modified|Removed|Renamed) \((\d+)\)/gm;
  let m;
  while ((m = re.exec(body))) {
    counts[m[1].toLowerCase()] = Number(m[2]);
  }
  return counts;
}

// Минимальный markdown → HTML для лида: достаточно для links + inline code,
// которые единственные встречаются в первом абзаце наших release-bodies.
// Экранируем HTML до подмен, чтобы юзерский markdown не пробил разметку.
function renderSummary(md) {
  const escaped = md
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');

  return escaped
    .replace(/`([^`]+)`/g, '<code>$1</code>')
    .replace(
      /\[([^\]]+)\]\(([^)]+)\)/g,
      '<a href="$2" target="_blank" rel="noopener noreferrer">$1</a>',
    )
    .replace(/\n+/g, ' ')
    .trim();
}

function transform(release) {
  const body = release.body || '';
  return {
    tag: release.tag_name,
    version: release.tag_name.replace(/^v/, ''),
    publishedAt: release.published_at,
    url: release.html_url,
    summaryHtml: renderSummary(extractSummary(body)),
    counts: extractCounts(body),
  };
}

async function main() {
  let entries = [];
  try {
    const releases = await fetchReleases();
    entries = releases.slice(0, COUNT).map(transform);
    console.log(`generate-changelog: fetched ${entries.length} releases`);
  } catch (err) {
    console.warn(`generate-changelog: failed to fetch releases (${err.message}) — emitting empty changelog`);
  }

  mkdirSync(dirname(out), { recursive: true });
  writeFileSync(
    out,
    `// Generated by scripts/generate-changelog.mjs — do not edit.\n` +
      `export interface ChangelogEntry {\n` +
      `  tag: string;\n` +
      `  version: string;\n` +
      `  publishedAt: string;\n` +
      `  url: string;\n` +
      `  summaryHtml: string;\n` +
      `  counts: { added?: number; modified?: number; removed?: number; renamed?: number };\n` +
      `}\n\n` +
      `export const CHANGELOG: ReadonlyArray<ChangelogEntry> = ${JSON.stringify(entries, null, 2)} as const;\n`,
  );
  console.log(`generate-changelog: wrote ${entries.length} entries -> src/generated/changelog.ts`);
}

main();
