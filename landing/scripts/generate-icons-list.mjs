#!/usr/bin/env node
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, '..');
const source = resolve(root, '../component/src/icon-names.types.ts');
const out = resolve(root, 'src/generated/icons.ts');

const src = readFileSync(source, 'utf8');
const names = [];
const re = /\|\s*'([^']+)'/g;
let match;
while ((match = re.exec(src)) !== null) {
  names.push(match[1]);
}

if (names.length === 0) {
  console.error(`generate-icons-list: no names parsed from ${source}`);
  process.exit(1);
}

mkdirSync(dirname(out), { recursive: true });
const body = names.map((n) => `  '${n}',`).join('\n');
writeFileSync(
  out,
  `// Generated by scripts/generate-icons-list.mjs — do not edit.\nexport const ICONS = [\n${body}\n] as const;\n\nexport type IconName = (typeof ICONS)[number];\n`,
);
console.log(`generate-icons-list: wrote ${names.length} icons -> src/generated/icons.ts`);
